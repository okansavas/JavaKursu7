package Gun09;

public class _04_JavaComparisonOperators {
    public static void main(String[] args) {
        int a=50;
        int b=50;

        System.out.println(" a > b " + (a>b)); // false : a, b den büyük mü
        System.out.println(" a >= b " + (a >=b)); // true : a, b den büyük esit mi
        System.out.println(" a < b " + ( a < b));  // false : a, b den kücük mü
        System.out.println(" a <= b " + ( a <= b)); // true : a, b den kucuk esit mi
        System.out.println(" a == b " + ( a==b)); // true : a, b ye esit mi
        System.out.println("a != b" + ( a!=b));  // false : a, b den farkli mi




    }
}
