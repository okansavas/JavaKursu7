public class _03_JavaAritmeticOperators {
    public static void main(String[] args) {
        int a=10;   // a nin degeri 10

        a=5;    // a nin degeri 5

        a=a+6;  // a nin degerini 6 artir : 11
        a+=6;   // a nin artis degeri 6  : 17

        a=a+1;  // a yi 1 artir : 18
        a++;   // a yi 1 artir : 19
        ++a;   // a yi 1 artir : 20

        a=a-5;  // a yi 5 azalt : 15
        a-=5;   // a yi 5 azalt : 10

        a=a*2;  // a yi 2 ile carp : 20
        a*=2;  // a nin carpim degeri 2 dir : 40;

        a=a/5;  // a yi 5 e böl : 8
        a/=2;  // a nin bölüm degeri 2 : 4




    }
}
