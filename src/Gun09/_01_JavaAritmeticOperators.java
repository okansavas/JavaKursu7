package Gun09;

public class _01_JavaAritmeticOperators {
    public static void main(String[] args) {
        int sayac=0;

        sayac = sayac+1;  // sayacin degerini 1 artirir: 1
        sayac++;  // sayacin degerini 1 artirir: 2
        ++sayac;  // sayacin degerini 1 artirir: 3

        sayac= sayac-1;  // sayacin degerini 1 azalt : 2
        sayac--;  // sayacin degerini 1 azalt : 1
        --sayac;  // sayacin degerini 1 azalt: 0




    }
}
